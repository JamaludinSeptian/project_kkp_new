    private void btnsimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnsimpanMouseClicked
        // TODO add your handling code here:
        String sql = "INSERT INTO tbl_kendaraan VALUES (?,?,?,?,?,?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, tkendaraan.getText());
            stat.setString(2, tmerek.getText());
            stat.setString(3, tmodel.getText());
            stat.setString(4, tnik.getText());
            stat.setString(5, tjenis.getText());
            stat.setString(6, twarna.getText());
            stat.setString(7, tpemilik.getText());
            stat.setString(8, tplat.getText());
            stat.setString(9, "1");
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null,"Data Berhasil Disimpan");
            kosong();
            tkendaraan.requestFocus();
            datatable();
        } catch (SQLException ex) {
            Logger.getLogger(HalamanAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnsimpanMouseClicked

    private void btnubahMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnubahMouseClicked
        // TODO add your handling code here:
        try {
            String sql = "UPDATE tbl_kendaraan set merek = ?, model=?, nik=?, jenis=?, warna=?, pemilik=?, platNomor=?, statusAktif=? where idKendaraan = ?";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, tmerek.getText());
            stat.setString(2, tmodel.getText());
            stat.setString(3, tnik.getText());
            stat.setString(4, tjenis.getText());
            stat.setString(5, twarna.getText());
            stat.setString(6, tpemilik.getText());
            stat.setString(7, tplat.getText());
            stat.setString(8, "1");
            stat.setString(9, tkendaraan.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil diubah");
            kosong();
            tkendaraan.requestFocus();
            datatable();
            btnsimpan.setEnabled(true);
        } catch (SQLException ex) {
             JOptionPane.showMessageDialog(null, "Data gagal diubah"+ex);
        }
    }//GEN-LAST:event_btnubahMouseClicked

