    protected void datatable(){
        Object[] Baris = {"ID Kendaraan", "Merek", "Model", "NIK", "Jenis", "Warna", "Pemilik", "Plat"};
        tabmode = new DefaultTableModel(null, Baris);
        tabelBarang.setModel(tabmode);
        String sql = "SELECT * FROM tbl_kendaraan";
       try {
           java.sql.Statement stat = conn.createStatement();
           ResultSet hasil = stat.executeQuery(sql);
           while(hasil.next()){
                String a = hasil.getString("idKendaraan");
                String b = hasil.getString("merek");
                String c = hasil.getString("model");
                String d = hasil.getString("nik");
                String e = hasil.getString("jenis");
                String f = hasil.getString("warna");
                String g = hasil.getString("pemilik");
                String h = hasil.getString("platNomor");
                String[] data = {a,b,c,d,e,f,g,h};
                tabmode.addRow(data);
            }
       } catch (SQLException ex) {
           Logger.getLogger(HalamanAdmin.class.getName()).log(Level.SEVERE, null, ex);
       }
            
    }
    
    
