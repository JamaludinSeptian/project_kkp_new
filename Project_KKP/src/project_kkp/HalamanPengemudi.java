    private void tabelPengemudiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelPengemudiMouseClicked
        // TODO add your handling code here:
        int bar = tabelPengemudi.getSelectedRow();
        String a = tabmode.getValueAt(bar, 0).toString();
        String b = tabmode.getValueAt(bar, 1).toString();
        String c = tabmode.getValueAt(bar, 2).toString();
        String d = tabmode.getValueAt(bar, 3).toString();
        String e = tabmode.getValueAt(bar, 4).toString();
        String f = tabmode.getValueAt(bar, 5).toString();
        String g = tabmode.getValueAt(bar, 6).toString();
        tidpengemudi.setText(a);
        tnama.setText(b);
        if(c.equals("Laki - Laki")){
            rLk.setSelected(true);
            rPr.setSelected(false);
        }else {
            rLk.setSelected(false);
            rPr.setSelected(true);
        }
        ttgl.setText(d);
        talamat.setText(e);
        tnohp.setText(f);
        tstatusaktif.setText(g);
        btnsimpan.setEnabled(false);
    }//GEN-LAST:event_tabelPengemudiMouseClicked

    private void btnhapusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnhapusMouseClicked
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null, "hapus", "Konfirmasi Dialog", JOptionPane.YES_NO_CANCEL_OPTION);
        if (ok==0){
            try {
                PreparedStatement stat = conn.prepareStatement("DELETE FROM tbl_pengemudi WHERE idPengemudi = ?");
                String noNya = tidpengemudi.getText();
                stat.setString(1, noNya);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null, "data berhasil dihapus");
                kosong();
                tidpengemudi.requestFocus();
                datatable();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Data gagal dihapus"+ex);
            }
        }
    }//GEN-LAST:event_btnhapusMouseClicked

