    protected void datatable(){
        Object[] Baris = {"ID Pengemudi","Nama","Jenis Kelamin","Tanggal Lahir","Alamat","No HP","Status Aktif"};
        tabmode = new DefaultTableModel(null, Baris);
        tabelPengemudi.setModel(tabmode);
        String sql = "SELECT * FROM tbl_pengemudi";
       try {
           java.sql.Statement stat = conn.createStatement();
           ResultSet hasil = stat.executeQuery(sql);
           while(hasil.next()){
                String a = hasil.getString("idPengemudi");
                String b = hasil.getString("nama");
                String c = hasil.getString("jenisKelamin");
                String d = hasil.getString("tglLahir");
                String e = hasil.getString("alamat");
                String f = hasil.getString("noHp");
                String g = hasil.getString("statusAktif");
                String[] data = {a,b,c,d,e,f,g};
                tabmode.addRow(data);
            }
       } catch (SQLException ex) {
           Logger.getLogger(HalamanAdmin.class.getName()).log(Level.SEVERE, null, ex);
       }
            
    }
    
    
