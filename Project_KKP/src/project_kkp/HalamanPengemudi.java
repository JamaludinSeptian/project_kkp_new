    private void btnsimpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnsimpanMouseClicked
        // TODO add your handling code here:
        String sql = "INSERT INTO tbl_pengemudi VALUES (?,?,?,?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, tidpengemudi.getText());
            stat.setString(2, tnama.getText());
            String jk = "";
            if(rLk.isSelected()) jk = "Laki - Laki";
            else jk = "Perempuan";
            stat.setString(3, jk);
            stat.setString(4, ttgl.getText());
            stat.setString(5, talamat.getText());
            stat.setString(6, tnohp.getText());
            stat.setString(7, tstatusaktif.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null,"Data Berhasil Disimpan");
            kosong();
            tidpengemudi.requestFocus();
            datatable();
        } catch (SQLException ex) {
            Logger.getLogger(HalamanAdmin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnsimpanMouseClicked

